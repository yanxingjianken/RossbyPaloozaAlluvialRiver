#!/usr/bin/env python3
"""Generate results_table.tex DIRECTLY from ../outputs/run_*.h5.

The results table must never be typed by hand: the moment a run is repeated with a
different parameter the hand-typed number becomes a fabrication that looks like data.
This script is the only writer of results_table.tex, and sw_sn_meander.tex \\inputs it.

    python make_results_table.py        # after experiments.py has finished
"""
import glob
import os

import h5py
import numpy as np

HERE = os.path.dirname(os.path.abspath(__file__))
OUT = os.path.join(HERE, "..", "outputs")
TEX = os.path.join(HERE, "results_table.tex")


K_REF = 1.80          # a wavenumber that is well resolved in EVERY run


def fastest_converged(f):
    """(k, sigma, c, k_is_converged) of the fastest mode with >=3 e-foldings.

    Reading the raw argmax of sigma would pick up modes whose 'growth rate' is a fit
    to transient adjustment; those are flagged disp_converged=0 by the driver.

    The 4th return value is the OTHER kind of convergence, and it is the one that bites:
    whether sigma(k) actually turns over inside the resolved band.  If the maximum sits
    at the grid edge, sigma_max is not a property of the equations -- it is a property of
    Ns, and it moves when you refine (it did: k_max 2.32 -> 4.72 and sigma 0.74 -> 1.49
    from Ns=64 to 128).  Such a row must be flagged, never quoted as a growth rate.
    """
    if "disp_k" not in f:
        return (np.nan,) * 3 + (False,)
    k, sg, c = f["disp_k"][:], f["disp_sigma"][:], f["disp_c"][:]
    ok = f["disp_converged"][:] > 0
    if not np.any(ok):
        return (np.nan,) * 3 + (False,)
    i = int(np.argmax(np.where(ok, sg, -np.inf)))
    kconv = bool(k[i] < 0.90 * k[ok].max())
    return float(k[i]), float(sg[i]), float(c[i]), kconv


def sigma_at(f, kref=K_REF):
    """sigma at a FIXED wavenumber -- the only like-for-like comparison across runs.

    Comparing each run's own sigma_max mixes converged numbers with grid-edge artifacts.
    """
    if "disp_k" not in f:
        return np.nan
    k, sg = f["disp_k"][:], f["disp_sigma"][:]
    return float(sg[int(np.argmin(np.abs(k - kref)))])


def main():
    rows = []
    for p in sorted(glob.glob(os.path.join(OUT, "run_*.h5"))):
        with h5py.File(p, "r") as f:
            a = f.attrs
            k, sg, c, kconv = fastest_converged(f)
            Ts = float(a.get("diag_T_shear", np.nan))
            Tb = float(a.get("diag_T_bend", np.nan))
            rows.append(dict(
                bed=("flat" if float(a["cross_amp"]) == 0
                     else f"{float(a['cross_amp']):.2g}"),
                bank=float(a["bank_sinuosity"]), Cf=float(a["Cf"]),
                U0=float(a["U0"]), Delta=float(a["Delta"]),
                k=k, sigma=sg, c=c, kconv=kconv, s_ref=sigma_at(f),
                div=float(a.get("diag_div_ratio", np.nan)),
                ratio=Ts / max(abs(Tb), 1e-300)))

    if not rows:
        raise SystemExit("no ../outputs/run_*.h5 -- run ../experiments.py first")

    L = [r"% AUTO-GENERATED by make_results_table.py -- do not edit by hand",
         r"\begin{tabular}{llllr|rrrrrr}", r"\hline",
         (r"bed $H$ & bank & $C_f$ & $U_0$ & $\Delta$ & "
          rf"$\sigma(k{{=}}{K_REF:g})$ & $k_{{\max}}$ & $\sigma_{{\max}}$ & $c$ "
          r"& $\|\delta'\|/\|\zeta'\|$ & $T_{\rm shear}/|T_{\rm bend}|$\\"), r"\hline"]
    for r in rows:
        if not np.isfinite(r["k"]):
            # nothing grew at all -- a STABLE configuration, which is a result, not a
            # missing number, and emphatically not a resolution problem
            kcell = scell = ccell = r"\emph{stable}"
        else:
            # a growth rate whose maximum sits at the grid edge is a property of Ns, not
            # of the equations: mark it so it can never be read as a converged number
            mk = "" if r["kconv"] else r"$^{\dagger}$"
            kcell = f"{r['k']:.2f}{mk}"
            scell = f"{r['sigma']:+.4f}{mk}"
            ccell = f"{r['c']:+.3f}"
        L.append(f"{r['bed']} & {r['bank']:.3g} & {r['Cf']:.3g} & {r['U0']:.2g} & "
                 f"{r['Delta']:+.2g} & {r['s_ref']:+.4f} & {kcell} & {scell} & "
                 f"{ccell} & {r['div']:.4f} & {r['ratio']:+.3f}\\\\")
    L += [r"\hline", r"\end{tabular}"]
    # a stable run has no k_max to be unconverged about -- don't count it as flagged
    nbad = sum(not r["kconv"] and np.isfinite(r["k"]) for r in rows)
    if nbad:
        L += [r"", (rf"\smallskip\noindent$^{{\dagger}}$\,{nbad} run(s): $\sigma(k)$ is still "
                    r"\emph{rising} at the last resolved wavenumber, so $k_{\max}$ and "
                    r"$\sigma_{\max}$ are set by $N_s$ and not by the equations (they moved "
                    r"from $k{=}2.32,\sigma{=}0.74$ to $k{=}4.72,\sigma{=}1.49$ when $N_s$ "
                    r"was doubled). Compare these rows only through the fixed-$k$ column.")]
    with open(TEX, "w") as fh:
        fh.write("\n".join(L) + "\n")

    npos = sum(r["ratio"] > 0 for r in rows)
    print(f"wrote {os.path.relpath(TEX, HERE)}  ({len(rows)} runs)")
    print(f"T_shear>0 (mean flow powers the wave) in {npos}/{len(rows)} runs")
    print(f"max |div_ratio| = {max(r['div'] for r in rows):.3g}  "
          f"(<<1 => balanced, not a gravity wave)")
    if nbad:
        print(f"WARNING: {nbad}/{len(rows)} runs have sigma(k) still rising at the grid "
              f"edge -- their k_max/sigma_max are resolution-limited and are flagged")
    print(f"sigma at the fixed reference k={K_REF:g}: "
          + ", ".join(f"{r['s_ref']:+.4f}" for r in sorted(rows, key=lambda r: -r["s_ref"])))


if __name__ == "__main__":
    main()
