#!/usr/bin/env python3
"""Generate results_table.tex DIRECTLY from ../outputs/run_*.h5.

The results table must never be typed by hand: the moment a run is repeated with a
different parameter the hand-typed number becomes a fabrication that looks like data.
This script is the only writer of results_table.tex, and sw_sn_meander.tex \\inputs it.

    python make_results_table.py        # after experiments.py has finished
"""
import glob
import os

import h5py
import numpy as np

HERE = os.path.dirname(os.path.abspath(__file__))
OUT = os.path.join(HERE, "..", "outputs")
TEX = os.path.join(HERE, "results_table.tex")


def fastest_converged(f):
    """(k, sigma, c) of the fastest mode that actually achieved >=3 e-foldings.

    Reading the raw argmax of sigma would pick up modes whose 'growth rate' is a fit
    to transient adjustment; those are flagged disp_converged=0 by the driver.
    """
    if "disp_k" not in f:
        return (np.nan,) * 3
    k, sg, c = f["disp_k"][:], f["disp_sigma"][:], f["disp_c"][:]
    ok = f["disp_converged"][:] > 0
    if not np.any(ok):
        return (np.nan,) * 3
    i = int(np.argmax(np.where(ok, sg, -np.inf)))
    return float(k[i]), float(sg[i]), float(c[i])


def main():
    rows = []
    for p in sorted(glob.glob(os.path.join(OUT, "run_*.h5"))):
        with h5py.File(p, "r") as f:
            a = f.attrs
            k, sg, c = fastest_converged(f)
            Ts = float(a.get("diag_T_shear", np.nan))
            Tb = float(a.get("diag_T_bend", np.nan))
            rows.append(dict(
                bed=("flat" if float(a["cross_amp"]) == 0
                     else f"{float(a['cross_amp']):.2g}"),
                bank=float(a["bank_sinuosity"]), Cf=float(a["Cf"]),
                U0=float(a["U0"]), Delta=float(a["Delta"]),
                k=k, sigma=sg, c=c,
                div=float(a.get("diag_div_ratio", np.nan)),
                ratio=Ts / max(abs(Tb), 1e-300)))

    if not rows:
        raise SystemExit("no ../outputs/run_*.h5 -- run ../experiments.py first")

    L = [r"% AUTO-GENERATED by make_results_table.py -- do not edit by hand",
         r"\begin{tabular}{llllr|rrrrr}", r"\hline",
         (r"bed $H$ & bank & $C_f$ & $U_0$ & $\Delta$ & $k_{\max}$ & $\sigma$ & $c$ "
          r"& $\|\delta'\|/\|\zeta'\|$ & $T_{\rm shear}/|T_{\rm bend}|$\\"), r"\hline"]
    for r in rows:
        L.append(f"{r['bed']} & {r['bank']:.3g} & {r['Cf']:.3g} & {r['U0']:.2g} & "
                 f"{r['Delta']:+.2g} & {r['k']:.2f} & {r['sigma']:+.4f} & "
                 f"{r['c']:+.3f} & {r['div']:.4f} & {r['ratio']:+.3f}\\\\")
    L += [r"\hline", r"\end{tabular}"]
    with open(TEX, "w") as fh:
        fh.write("\n".join(L) + "\n")

    npos = sum(r["ratio"] > 0 for r in rows)
    print(f"wrote {os.path.relpath(TEX, HERE)}  ({len(rows)} runs)")
    print(f"T_shear>0 (mean flow powers the wave) in {npos}/{len(rows)} runs")
    print(f"max |div_ratio| = {max(r['div'] for r in rows):.3g}  "
          f"(<<1 => balanced, not a gravity wave)")


if __name__ == "__main__":
    main()
